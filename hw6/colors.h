/*
    Some ascii color codes to pretty print strings with colors
*/

#ifndef COLORS_H
#define COLOR_H

#define BLACK   "\e[0;30m"
#define RED     "\e[0;31m"
#define GREEN   "\e[0;32m"
#define YELLLOW "\e[0;33m"
#define BLUE    "\e[0;34m"
#define MAGENTA "\e[0;35m"
#define CYAN    "\e[0;36m"
#define WHITE   "\e[0;37m"
#define RESET   "\e[0m"

#endif  // COLORS_H